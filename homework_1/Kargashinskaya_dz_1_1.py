duration = 60887675
d = duration // 86400  # количество дней
if d > 0:
    print(d, 'дн.')
sec = duration % 86400  # остаток от деления на 86400
if sec > 0:
    h = sec // 3600  # определяем количество часов
    if h > 0:
        print(h, 'час.')
x = sec % 3600  # остаток от часов
if x >= 60:
    m = x // 60  # определяем минуты
    print(m, 'мин.')
    c = x % 60  # определяем секунды
    if c > 0:
        print(c, 'сек.')
elif 0 <= x < 60:
    print(x, 'сек')
